const int MN = 2002, mod = 1e9+7;
int N, s, e, dp[2][MN], i, j, k;

int main(){
    scanf("%d%d%d",&N,&s,&e);
    dp[0][0]=1;
    for(i=1;i<N;i++){
        memset(dp[i&1],0,sizeof(dp[i&1]));
        if(i==s||i==e) k++;
        for(j=0;j<=N;j++){
            if(i!=s&&i!=e) dp[i&1][j]=((j?dp[(i+1)&1][j-1]:0)+(1LL*(j+1)*(j+k)%mod*dp[(i+1)&1][j+1]))%mod;
            else dp[i&1][j]=(dp[(i+1)&1][j]+(1LL*(j+1)*dp[(i+1)&1][j+1]))%mod;
        }
    }
    printf("%d\n",dp[(N+1)&1][0]);
    return 0;
}