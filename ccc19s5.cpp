const int MN = 3003;
int N, K, i, j, sp[MN][MN], k, c; ll ans;

int main(){
    for(scanf("%d%d",&N,&K),i=1;i<=N;i++){
        for(j=1;j<=i;j++) scanf("%d",&sp[N-i+1][j]);
    }
    k = 1;
    while(k<K){
        c = min(K-k,(int)ceil(k/2.0));
        for(i=1;i<=N-c-k+1;i++){
            for(j=1;j<=N-i-c-k+2;j++){
                sp[i][j]=max(sp[i][j],max(sp[i+c][j],sp[i][j+c]));
            }
        }
        k += c;
    }
    for(i=1;i<=N-k+1;i++){
        for(j=1;j<=N-i-k+2;j++){
            ans += sp[i][j];
        }
    }
    printf("%lld\n",ans);
    return 0;
}