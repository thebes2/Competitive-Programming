const int MN = 5005;
int N, M, Q, psa[MN*MN], i, j, x;

int main(){
    scanf("%d%d%d",&N,&M,&Q);
    for(i=1;i<=N;i++){
        for(j=1;j<=M;j++){
            int mn=i*j;
            int mx=N*M+1-(N-i+1)*(M-j+1);
            psa[mn]++, psa[mx+1]--;
        }
    }
    for(i=1;i<=N*M;i++)
        psa[i+1]+=psa[i];
    while(Q--){
        scanf("%d",&x);
        printf("%d\n",psa[x]);
    }
    return 0;
}